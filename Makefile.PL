    ($] >= 5.005 ?    ## Add these new keywords supported since 5.005
      (ABSTRACT_FROM => 'Simple.pm', # retrieve abstract from module
       AUTHOR     => 'Jonathan Chin <jon-opengl-simple@earth.li>') : ()),
    @flags
